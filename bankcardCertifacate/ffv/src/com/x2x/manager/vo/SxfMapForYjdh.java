package com.x2x.manager.vo;

import java.io.Serializable;

public class SxfMapForYjdh implements Serializable{
	private static final long serialVersionUID = 7545376780713743036L;
	private Integer sxf;//交易手续费率
	private Integer fee;//交易手续费金额
	private Integer isFeeType;//'交易手续费计费模式，10：比率；20：固定金额；默认值为10',
	private Integer drawOutSxfFee;//提现手续费率
	private Integer drawOutSxfRate;//提现手续费金额
	private Integer drawOutFee;//提现手续费金额
	private Integer drawOutIsFeeType;//'提现手续费计费模式，10：比率；20：固定金额；默认值为10',
	public SxfMapForYjdh() {
		super();
	}
	public SxfMapForYjdh(Integer sxf, Integer fee, Integer isFeeType,
			Integer drawOutSxfFee, Integer drawOutSxfRate, Integer drawOutFee,
			Integer drawOutIsFeeType) {
		super();
		this.sxf = sxf;
		this.fee = fee;
		this.isFeeType = isFeeType;
		this.drawOutSxfFee = drawOutSxfFee;
		this.drawOutSxfRate = drawOutSxfRate;
		this.drawOutFee = drawOutFee;
		this.drawOutIsFeeType = drawOutIsFeeType;
	}
	public Integer getSxf() {
		return sxf;
	}
	public void setSxf(Integer sxf) {
		this.sxf = sxf;
	}
	public Integer getFee() {
		return fee;
	}
	public void setFee(Integer fee) {
		this.fee = fee;
	}
	public Integer getIsFeeType() {
		return isFeeType;
	}
	public void setIsFeeType(Integer isFeeType) {
		this.isFeeType = isFeeType;
	}
	public Integer getDrawOutSxfFee() {
		return drawOutSxfFee;
	}
	public void setDrawOutSxfFee(Integer drawOutSxfFee) {
		this.drawOutSxfFee = drawOutSxfFee;
	}
	public Integer getDrawOutSxfRate() {
		return drawOutSxfRate;
	}
	public void setDrawOutSxfRate(Integer drawOutSxfRate) {
		this.drawOutSxfRate = drawOutSxfRate;
	}
	public Integer getDrawOutFee() {
		return drawOutFee;
	}
	public void setDrawOutFee(Integer drawOutFee) {
		this.drawOutFee = drawOutFee;
	}
	public Integer getDrawOutIsFeeType() {
		return drawOutIsFeeType;
	}
	public void setDrawOutIsFeeType(Integer drawOutIsFeeType) {
		this.drawOutIsFeeType = drawOutIsFeeType;
	}
}
